function menuFunc() {
  let menuBtn = document.querySelector("[menu-btn-container]");
  let menuContainer = document.querySelector(".menu-container");
  let slices = document.querySelectorAll(".menu-container .slice");
  let listItems = document.querySelectorAll(".slices-container + div li");
  
  menuBtn.addEventListener("click", _ => {
    slices.forEach(slice => slice.classList.toggle("shown-slice"));
    listItems.forEach(e => e.classList.toggle("shown-li"));
    menuContainer.classList.toggle("show-menu");
  });
}








