function createTransitionSlices() {
    let content = `
    <div class="transition-wrapper position-fixed z-1" inert>
      <div class="slice" style="--delay: .3s;"></div>
      <div class="slice" style="--delay: .4s;"></div>
      <div class="slice" style="--delay: .5s;"></div>
      <div class="slice" style="--delay: .6s;"></div>
    </div>
    `;
    document.body.insertAdjacentHTML('beforeend', content);
}


function transition() {
    let links = document.querySelectorAll("a");
    let translices = document.querySelectorAll(".transition-wrapper div");
    
    links.forEach(link => link.addEventListener("click", e => {
        e.preventDefault();

        translices.forEach(slice => slice.classList.add("shown-slice"));
        
        setTimeout( _ => window.location.href = link.href, 600); 
    }));
}

